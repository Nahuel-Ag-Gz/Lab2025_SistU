    private void ButtbuscardocumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtbuscardocumentoActionPerformed
String docBuscado = txtDocumentoBuscar.getText().trim();

    if (docBuscado.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor ingrese un documento.");
        return;
    }

    boolean encontrado = false;

    // Recorrer todos los paneles dentro del contenedor que tiene a las personas
    for (Component comp : Plistapersonas.getComponents()) {
        if (comp instanceof JPanel panelPersona) {
            for (Component subComp : panelPersona.getComponents()) {
                if (subComp instanceof JLabel label) {
                    String texto = label.getText().replaceAll("<[^>]+>", ""); // Quita etiquetas HTML como <b>
                    if (texto.startsWith("Documento:")) {
                        String docEnPanel = texto.replace("Documento:", "").trim();
                        if (docEnPanel.equals(docBuscado)) {
                            // Hace scroll hasta ese panel
                            panelPersona.scrollRectToVisible(new Rectangle(panelPersona.getSize()));
                            resaltarPanel(panelPersona);
                            encontrado = true;
                            break;
                        }
                    }
                }
            }
        }
        if (encontrado) break;
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(this, "No se encontró ninguna persona con ese documento.");
    }
    }//GEN-LAST:event_ButtbuscardocumentoActionPerformed

