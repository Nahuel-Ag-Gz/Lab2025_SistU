    private void ButtcargapersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtcargapersonaActionPerformed
        String tipo = (String) Stipopersona.getSelectedItem();
        String nombre = Tnombre.getText().trim();
        String direccion = Tdireccion.getText().trim();
        String documento = Tdocumento.getText().trim();
        String facultad = Tfacultad.getText().trim();
        String carrera = Tcarrera.getText().trim();
        String registro = Tregistro.getText().trim();

        if (nombre.isEmpty() || documento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos obligatorios.");
            return;
        }

        int diaNac, mesNac, anioNac, diaIng, mesIng, anioIng;
        try {
            diaNac = Integer.parseInt(Tdia.getText());
            mesNac = Integer.parseInt(Tmes.getText());
            anioNac = Integer.parseInt(Tanio.getText());
            diaIng = Integer.parseInt(Tdiaing.getText());
            mesIng = Integer.parseInt(Tmesing.getText());
            anioIng = Integer.parseInt(Tanioing.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Las fechas deben contener solo números.");
            return;
        }

        // Validar fechas con método externo
        if (!fechaValida(diaNac, mesNac, anioNac)) {
            JOptionPane.showMessageDialog(this, "Fecha de nacimiento inválida.");
            return;
        }
        if (!fechaValida(diaIng, mesIng, anioIng)) {
            JOptionPane.showMessageDialog(this, "Fecha de ingreso inválida.");
            return;
        }

        // Validar que ingreso no sea antes que nacimiento
        LocalDate fechaNacimiento = LocalDate.of(anioNac, mesNac, diaNac);
        LocalDate fechaIngreso = LocalDate.of(anioIng, mesIng, diaIng);
        if (fechaIngreso.isBefore(fechaNacimiento)) {
            JOptionPane.showMessageDialog(this, "La fecha de ingreso no puede ser anterior a la fecha de nacimiento.");
            return;
        }

        // Crear arrays de fechas
        int[] fechaNac = {diaNac, mesNac, anioNac};
        int[] fechaIngre = {diaIng, mesIng, anioIng};

        Persona nuevaPersona = null;
        switch (tipo) {
            case "Alumno" -> {
                nuevaPersona = new Alumno(nombre, documento, direccion, fechaNac, facultad, carrera, fechaIngre, registro);
            }
            case "Alumno de Postgrado" -> {
                String carreraPG = Tcarrerapostgrado.getText().trim();
                nuevaPersona = new AlumnoPostGrado(nombre, documento, direccion, fechaNac, facultad, carrera, fechaIngre, registro, carreraPG);
            }
            case "Docente" -> {
                String cargo = Tcargo.getText().trim();
                int count = modeloMaterias.getSize();
                ArrayList<String> materias = new ArrayList<>();
                for (int i = 0; i < count; i++) {
                    materias.add(modeloMaterias.getElementAt(i));
                }
                nuevaPersona = new Docente(nombre, documento, direccion, fechaNac, facultad, carrera, fechaIngre, cargo, materias);
            }
            default -> {
                JOptionPane.showMessageDialog(this, "Tipo de persona no reconocido.");
                return;
            }
        }

        if (!lista.isfull()) {
            if (!lista.inStruc(nuevaPersona)) {
                lista.insert(nuevaPersona);

                System.out.println("Persona cargada con éxito: " + nuevaPersona);
                JOptionPane.showMessageDialog(this, "Persona cargada con éxito.");
                modeloMaterias.clear(); // Limpiar materias si era un docente
                limpiarCampos(); // ← limpia todos los campos luego de insertar
            } else {
                JOptionPane.showMessageDialog(this, "Esta persona ya está registrada.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "La lista está llena.");
        }
    }//GEN-LAST:event_ButtcargapersonaActionPerformed

