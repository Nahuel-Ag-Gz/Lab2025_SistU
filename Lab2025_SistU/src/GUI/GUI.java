private String obtenerTipoPersona(Persona p) {
    if (p instanceof AlumnoPostGrado) return "Alumno Postgrado";
    if (p instanceof Alumno) return "Alumno";
    if (p instanceof Docente) return "Docente";
    if (p instanceof PersUniv) return "Personal Universitario";
    return "Tipo desconocido";
}

public void mostrarListaEnGUI(ListaPersonas lista) {
    Plistapersonas.removeAll();
    Plistapersonas.setLayout(new BoxLayout(Plistapersonas, BoxLayout.Y_AXIS));

    lista.reset();
    while (!lista.oos()) {
        Persona p = lista.copy();
        JPanel nuevo = clonarPanelDesdeTemplate(p, lista);
        Plistapersonas.add(nuevo);
        lista.forward();
    }

    Plistapersonas.revalidate();
    Plistapersonas.repaint();
}
  private void resaltarPanel(JPanel panel) {
    Color original = panel.getBackground();
    Color resaltado = new Color(0,128,176 );  // Amarillo claro

    Timer timer = new Timer(300, null); // Cada 300ms
    final int[] contador = {0};

    timer.addActionListener(e -> {
        if (contador[0] % 2 == 0) {
            panel.setBackground(resaltado);
        } else {
            panel.setBackground(original);
        }

        contador[0]++;
        if (contador[0] >= 6) { // 3 ciclos de parpadeo (6 cambios)
            panel.setBackground(original);
            timer.stop();
        }
    });

    timer.start();
}






